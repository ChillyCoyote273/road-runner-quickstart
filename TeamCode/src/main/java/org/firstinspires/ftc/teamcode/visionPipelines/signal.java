package org.firstinspires.ftc.teamcode.visionPipelines;


public class signal {
    Color color;
    double xPos;
    double yPos;
    double area;
    double perimeter;
    double ratio;
    public signal(Color color, double xPos, double yPos, double area, double perimeter, double ratio) {
        this.color = color;
        this.xPos = xPos;
        this.yPos = yPos;
        this.area = area;
        this.perimeter = perimeter;
        this.ratio = ratio;
    }

}